function [NN] = getNN(r, s, t)
    
N1 = 1/8*((1-r)*(1-s)*(1+t));
N2 = 1/8*((1-r)*(1-s)*(1-t));
N3 = 1/8*((1-r)*(1+s)*(1-t));
N4 = 1/8*((1-r)*(1+s)*(1+t));

N5 = 1/8*((1+r)*(1-s)*(1+t));
N6 = 1/8*((1+r)*(1-s)*(1-t));
N7 = 1/8*((1+r)*(1+s)*(1-t));
N8 = 1/8*((1+r)*(1+s)*(1+t));

N=[N1*eye(3) N2*eye(3) N3*eye(3) N4*eye(3) N5*eye(3) N6*eye(3)...
    N7*eye(3) N8*eye(3)];

NN=N'*N;
    
end